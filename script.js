let currentPage = 1;

function updatePage() {
  const img = document.getElementById("photo");
  const caption = document.getElementById("caption");
  img.src = `images/page${currentPage}.jpg`;
  caption.textContent = captions[currentPage - 1];
}

function nextPage() {
  if (currentPage < captions.length) {
    currentPage++;
    updatePage();
  }
}

function prevPage() {
  if (currentPage > 1) {
    currentPage--;
    updatePage();
  }
}
